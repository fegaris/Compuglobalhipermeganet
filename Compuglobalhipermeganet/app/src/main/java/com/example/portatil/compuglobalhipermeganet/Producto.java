public class Producto {

        String idProducto;
        String nombre;
        String descripcionBreve;
        String detalle;
        String idFoto;
        float precio;

    Producto(String idProducto, String nombre, String descripcionBreve, String detalle, String idFoto, float precio) {
            this.idProducto = idProducto;
            this.nombre = nombre;
            this.descripcionBreve = descripcionBreve;
            this.detalle = detalle;
            this.idFoto = idFoto;
            this.precio = precio;
        }
    }







